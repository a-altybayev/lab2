# Tuples - ordered, unchangeable, allow duplicate values, can contain different types
mytuple = ("apple", "banana", "cherry")
print(mytuple)

# Tuple Lenght 
thistuple = ("apple", "banana", "cherry")
print(len(thistuple))

# Create Tuple With One Item

thistuple = ("apple",)
print(type(thistuple))

#NOT a tuple
thistuple = ("apple")
print(type(thistuple))


# type()
mytuple = ("apple", "banana", "cherry")
print(type(mytuple)) # <class 'tuple'>
