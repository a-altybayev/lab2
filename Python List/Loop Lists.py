# Loop Through a List
thislist = ["apple", "banana", "cherry"]
for x in thislist:
    print(x)


# Loop Through the Index Numbers
thislist = ["apple", "banana", "cherry"]
for i in range(len(thislist)):
  print(thislist[i])


# Loop Using List Comprehension
thislist = ["apple", "banana", "cherry"]
[print(x) for x in thislist]