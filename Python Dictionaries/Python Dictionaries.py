# Dictionaries - key:value pairs // ordered, changable, do NOT allow diplicates
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print(thisdict)


# Dictionary Items
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print(thisdict["brand"])

# <class 'dict'>

# The dict() Constructor
thisdict = dict(name = "Jhonotan", age = 21, country = "Norway")
print(thisdict)